class Solution:
    def updateMatrix(self, mat: List[List[int]]) -> List[List[int]]:
        result = np.ones((len(mat), len(mat[0])), int) * -1
        result = result * mat
        distance = 1
        positions = list()
        for row_num, row in enumerate(result):
            for col_num, item in enumerate(row):
                if item == 0:
                    positions.append((row_num, col_num))
        while positions:
            new_positions = list()
            for row_num, col_num in positions:
                new_position = self._update_neighbors(
                    row_num, col_num, result, distance
                )
                new_positions += new_position
            positions = new_positions
            distance += 1

        return result.tolist()

    def _update_neighbors(self, row_num, col_num, result, distance):
        positions = list()
        for row_index in [row_num - 1, row_num + 1]:
            if (
                row_index >= 0
                and row_index < len(result)
                and (
                    result[row_index][col_num] == -1
                    or result[row_index][col_num] > distance
                )
            ):
                result[row_index][col_num] = distance
                positions.append((row_index, col_num))
        for col_index in [col_num - 1, col_num + 1]:
            if (
                col_index >= 0
                and col_index < len(result[0])
                and (
                    result[row_num][col_index] == -1
                    or result[row_num][col_index] > distance
                )
            ):
                result[row_num][col_index] = distance
                positions.append((row_num, col_index))
        return positions


